class Solution {
public:
    long long maxProduct(vector<int>& nums) {
        int n = nums.size();

        int zeros = 0;
        for (int i=0;i<nums.size();i++) {
            if (nums[i] < 0) nums[i] = -1*nums[i];
            if (nums[i] == 0) zeros++;
        }
        if (zeros > 1 && zeros > n - 3) return 0;

        sort(nums.begin(),nums.end());

        long long res = 1;

        for (int i=n-1;i>=n-2;i--) {
            res *= nums[i];
        }
        if (res > 0) res *= 100000;
        else res *= -100000;
        return res;
    }
};

